from typing import Final


class COLOR:
    BLACK: Final[str] = "\033[30m"  # (文字)黒
    RED: Final[str] = "\033[31m"  # (文字)赤
    GREEN: Final[str] = "\033[32m"  # (文字)緑
    YELLOW: Final[str] = "\033[33m"  # (文字)黄
    BLUE: Final[str] = "\033[34m"  # (文字)青
    RESET: Final[str] = "\033[0m"  # リセット
